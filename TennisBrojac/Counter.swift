import Foundation

class Counter {
    var count = 0
    var count0 = 0
    
    func incrementByOne() -> Int {
        count0 += 1
        return count0
    }

    func increment() -> Int {
        count += 15
        return count
    }
    func decrement() -> Int {
        count -= 5
        return count
    }
    func decrementBy() -> Int {
        count -= 15
        return count
        
    }

    
    func reset() -> Int {
        count = 0
        
        return count
    }
    func reset0() -> Int {
        count0 = 0
    return count0

    }
}

